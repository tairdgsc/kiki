    private void jButtonCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCadastrarActionPerformed
        int status = 0;
        mySQLcon.conectaBanco();

        novoUsuario = new Usuario();
        novoUsuario.setNome(jTextFieldNome.getText());
        novoUsuario.setEmail(jTextFieldEmail.getText());
        novoUsuario.setSenha(new String(jPasswordFieldSenha.getPassword()));

        try {
            status = this.mySQLcon.insertSQL("INSERT INTO usuario ("
                    + "nome,"
                    + "email,"
                    + "senha"
                    + ") VALUES ("
                    + "'" + novoUsuario.getNome() + "',"
                    + "'" + novoUsuario.getEmail() + "',"
                    + "'" + novoUsuario.getSenha() + "'"
                    + ");");

            if (status == 1) {
                JOptionPane.showMessageDialog(null, "Usuario cadastrado com sucesso!", "SUCESSO", JOptionPane.INFORMATION_MESSAGE, null);
            } else {
                JOptionPane.showMessageDialog(null, "Houve algum problema ao cadastrar usuario.", "ERRO", JOptionPane.ERROR_MESSAGE, null);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Houve um erro no cadastro do usuario");
        } finally {
            mySQLcon.fechaBanco();
            this.setVisible(false);
        }
    }//GEN-LAST:event_jButtonCadastrarActionPerformed

